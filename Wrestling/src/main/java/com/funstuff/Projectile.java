package com.funstuff;

public class Projectile {
	double x;
	double y;
	double degRot;
	double mag;
	
	public Projectile(){
		
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public double getDegRot() {
		return degRot;
	}

	public void setDegRot(double degRot) {
		this.degRot = degRot;
	}

	public double getMag() {
		return mag;
	}

	public void setMag(double mag) {
		this.mag = mag;
	}
	
}
